
public class Game {

	int gameId;
	double price;
	String name, platform, genre, publisherOrDeveloper;

	public int getGameId() {
		return gameId;
	}

	public void setGameId(int gameId) {
		this.gameId = gameId;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPlatform() {
		return platform;
	}

	public void setPlatform(String platform) {
		this.platform = platform;
	}

	public String getGenre() {
		return genre;
	}

	public void setGenre(String genre) {
		this.genre = genre;
	}

	public String getPublisherOrDeveloper() {
		return publisherOrDeveloper;
	}

	public void setPublisherOrDeveloper(String publisherOrDeveloper) {
		this.publisherOrDeveloper = publisherOrDeveloper;
	}

}
